//  start     ; stop  ; step
for (let i = 1; i <= 5; i++) {
  console.log(i);
}

// White Loop

let count = 1;
while (count <= 5) {
  console.log(count);
  count++; // count += 2 (count = count + 2)
}

// Do While
let number = 1;
do {
  console.log(number);
  number++;
} while (number <= 5);

